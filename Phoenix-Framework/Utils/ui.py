def log(text, alert=""):
    style = ""
    # log type is choosen by alert
    if alert == "info":
        style = "blue"
    elif alert == "success":
        style = "green"
    elif alert == "warning":
        style = "yellow"
    elif alert == "error":
        style = "red"
    elif alert == "critical":
        style = "#9e0909"
    console.print("[" + alert.upper() + "] " + text, style=style)


